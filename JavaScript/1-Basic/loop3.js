/*
    syntax for while loop
    init;
    while(condition)
    {
        statement;
        ++/--
    }
 */
let i=2015;
while(i<=2023){
    console.log("its year -:",i);
    i++;
}

console.log("-------while loop in reverse order--------");
let j=2023;
while(j>=2015){
    console.log("its year -:",j);
    j--;
}